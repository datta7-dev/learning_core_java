public class dataType {
	
	public static void main(String args[]) {
		
		int a=10;// size- 4 bytes;
		short b=20;// size- 2 bytes;
		float c=30;// size- 4 bytes;
		char d='d';// size- 2 bytes;
		String str="Datta";
		double e=50;// size- 8 bytes;
		long l=60;// size- 8 bytes;
		boolean t=true;// size- 1 bytes;
		
		System.out.println(a +" "+ b +" "+ c +" "+ d +" "+ str +" "+ e +" "+ l +" "+ t);
	}

}
